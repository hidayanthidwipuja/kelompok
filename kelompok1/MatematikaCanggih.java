public class MatematikaCanggih {
    
    
    void pertambahan(int a, int b){
        //Method pertambahan menerima dua parameter int dan mengembalikan hasil penjumlahan
        System.out.println("Hasil pertambahan (10 + 10):" + (a+b));
    }
    void perkalian(int a, int b){
        //Method perkalian menerima dua parameter int dan mengembalikan hasil perkalian
        System.out.println("Hasil perkalian(50 * 10):" + (a * b));
    }
    void modulus(int a, int b){
        //Method modulus mengembalikan sisa hasil pembagian antara dua bilangan (a % b).
        System.out.println("Hasil modulus (9 % 2):" + (a % b));
    }
    void pengurangan(int a, int b){
        System.out.println("Hasil pengurangan (10 - 5):" + (a - b));
    } 
    void pembagian(int a, int b){
        System.out.println("Hasil pembagian (10 / 5):" + (a / b));
    }
}
